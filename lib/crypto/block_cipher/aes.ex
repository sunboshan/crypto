  @doc """
  Encrypt a giving 128-bit binary with 128-bit key using AES.

  Example:

      iex> AES.encrypt("1234567887654321", "abcdefghijklmnop")
      <<50, 216, 214, 227, 0, 56, 127, 180, 132, 148, 147, 209, 233, 250, 227, 199>>
  """
  @spec encrypt(<<_::128>>, <<_::128>>) :: <<_::128>>
  def encrypt(key, binary) when bit_size(key) == 128 and bit_size(binary) == 128 do
    # initial round
    [k0 | keys] = subkeys(key)
    r0 = add_roundkey(binary, k0)

    # round 1-9
    {r9, [key]} = Enum.reduce(1..9, {r0, keys}, fn _, {bin, [key | t]} ->
      new_bin = substitute(bin)
        |> shift_row()
        |> mix_column()
        |> add_roundkey(key)
      {new_bin, t}
    end)

    # final round
    substitute(r9)
    |> shift_row()
    |> add_roundkey(key)
  end

  defp subkeys(key) do
    Stream.unfold({key, @rc}, fn {key, [rc | t]} ->
      <<w0::32, w1::32, w2::32, w3::32>> = key
      <<a, b, c, d>> = <<w3::32>>
      <<ss::32>> = substitute(<<b, c, d, a>>)
      w4 = ss ^^^ w0 ^^^ rc
      w5 = w4 ^^^ w1
      w6 = w5 ^^^ w2
      w7 = w6 ^^^ w3
      new_key = <<w4::32, w5::32, w6::32, w7::32>>
      {key, {new_key, t}}
    end)
    |> Enum.take(11)
  end

  defp add_roundkey(bin, key) do
    a = for <<a <- bin>>, do: a
    b = for <<b <- key>>, do: b
    for {a, b} <- Enum.zip(a, b), into: <<>>, do: <<a ^^^ b>>
  end

  defp substitute(bin) do
    for <<i::4, j::4 <- bin>>, into: <<>>, do: <<elem(elem(@s, i), j)>>
  end

  defp shift_row(bin) do
    <<b0, b1, b2, b3,
      b4, b5, b6, b7,
      b8, b9, ba, bb,
      bc, bd, be, bf>> = bin
    <<b0, b5, ba, bf,
      b4, b9, be, b3,
      b8, bd, b2, b7,
      bc, b1, b6, bb>>
  end

  defp mix_column(bin) do
    for <<a, b, c, d <- bin>>, <<i, j, k, l <- @matrix>>, into: <<>> do
      <<gf(i, a) ^^^ gf(j, b) ^^^ gf(k, c) ^^^ gf(l, d)>>
    end
  end

  defp gf(0, _), do: 0
  defp gf(_, 0), do: 0
  defp gf(1, m), do: m
  defp gf(n, m) do
    <<a::4, b::4>> = <<n>>
    <<i::4, j::4>> = <<m>>
    x = elem(elem(@l, a), b)
    y = elem(elem(@l, i), j)
    z = x + y
    z = if z > 0xff, do: z - 0xff, else: z
    <<a::4, b::4>> = <<z>>
    elem(elem(@e, a), b)
  end
